        plt.title('Original Image 1 (Step t)', fontsize=12)
        plt.axis('off')
        
        plt.subplot(3, 4, 2)
        plt.imshow(img2, cmap='gray')
        plt.title('Original Image 2 (Step t+1)', fontsize=12)
        plt.axis('off')
        
        # Image difference
        plt.subplot(3, 4, 3)
        diff_img = np.abs(img1.astype(float) - img2.astype(float))
